    //Método para preencher a tabela
    public void preencherCheckIn(List<Reserva> reservas) {
        if (reservas.isEmpty()) {
            LocalDate hoje = LocalDate.now();
            DateTimeFormatter formatter = DateTimeFormatter.ofPattern("dd/MM/y");
            String dataFormatada = hoje.format(formatter);
            
            JOptionPane.showMessageDialog(rootPane, "Não há reservas confirmadas para hoje '" 
                    + dataFormatada + "'!", "SEM RESERVAS", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        String colunas[] = {"Id", "Nome", "Check-In", "Check-Out", "Quarto", "Total Dias", "Valor total", "Status"};
        String dados[][] = new String [reservas.size()][colunas.length];
        
        DateTimeFormatter formatter = DateTimeFormatter.ofPattern("dd/MM/y");
        NumberFormat formatoMoeda = NumberFormat.getCurrencyInstance(new Locale("pt", "BR"));
        
        int i = 0;
        for(Reserva r: reservas) {
            String totalFormatado = formatoMoeda.format(r.getValorTotal());
            
            dados[i] = new String[]{
                String.valueOf(r.getId()),
                r.getHospede().getNome(),
                r.getCheckin().format(formatter),
                r.getCheckout().format(formatter),
                r.getQuarto().getNumero(),
                String.valueOf(r.getTotalDias()),
                totalFormatado,
                r.getEstatus()
            };
            i++;
        }
        DefaultTableModel model = new DefaultTableModel(dados, colunas);
        tblCheckIn.setModel(model);
        
        //Configura a fonte do cabeçalho
        tblCheckIn.getTableHeader().setFont(new Font("Segoe UI", Font.BOLD, 12));
        
        //Ajusta a largura das colunas
        tblCheckIn.getColumnModel().getColumn(0).setPreferredWidth(60);
        tblCheckIn.getColumnModel().getColumn(1).setPreferredWidth(250);
        tblCheckIn.getColumnModel().getColumn(2).setPreferredWidth(75);
        tblCheckIn.getColumnModel().getColumn(3).setPreferredWidth(75);
        tblCheckIn.getColumnModel().getColumn(4).setPreferredWidth(55);
        tblCheckIn.getColumnModel().getColumn(5).setPreferredWidth(60);
        tblCheckIn.getColumnModel().getColumn(6).setPreferredWidth(100);
        tblCheckIn.getColumnModel().getColumn(7).setPreferredWidth(85);
    }
