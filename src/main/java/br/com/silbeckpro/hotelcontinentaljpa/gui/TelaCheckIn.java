    private void btnConfirmarCheckInActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmarCheckInActionPerformed
        int linhaSelecionada = tblCheckIn.getSelectedRow();
        if (linhaSelecionada != - 1) {
            
            long reservaId = Long.parseLong((String) tblCheckIn.getValueAt(linhaSelecionada, 0));
            int resposta = JOptionPane.showConfirmDialog(rootPane, "Confirma Check-In?", "CHECK-IN", JOptionPane.YES_NO_OPTION);
            
            if (resposta == JOptionPane.YES_OPTION) {
                ReservasDAO reservaDAO = new ReservasDAO();
                reservaDAO.fazerCheckIn(reservaId);
                
                JOptionPane.showMessageDialog(rootPane, "Check-In realizado com sucesso!", "SUCESSO", JOptionPane.INFORMATION_MESSAGE);
                carregarCheckIn();
                
                if (listaCheckin.isEmpty()) {
                    dispose();
                } 
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "Atenção: selecione a reserva para realizar o Check-In!", "ATENÇÃO", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnConfirmarCheckInActionPerformed
